CREATE TABLE IF NOT EXISTS jippy_fm.area
(
    area_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    area_name character varying(50),
    city_id integer NOT NULL,
    created_at timestamp without time zone,
    created_by integer,
    updated_at timestamp without time zone,
    updated_by integer,
    CONSTRAINT area_pkey PRIMARY KEY (area_id),
    CONSTRAINT area_city_fk_key FOREIGN KEY (city_id)
