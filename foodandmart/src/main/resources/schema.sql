CREATE TABLE IF NOT EXISTS jippy_fm.status
(
    status_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    status_name character varying(50),
    created_at timestamp without time zone,
    created_by integer,
    updated_at timestamp without time zone,
    updated_by integer,
    CONSTRAINT status_pkey PRIMARY KEY (status_id)
);


CREATE TABLE IF NOT EXISTS jippy_fm.state
(
    state_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    state_name character varying(50),
    created_at timestamp without time zone,
    created_by integer,
    updated_at timestamp without time zone,
    updated_by integer,
    CONSTRAINT state_pkey PRIMARY KEY (state_id)
);

CREATE TABLE IF NOT EXISTS jippy_fm.city
(
    city_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    city_name character varying(50),
    state_id integer NOT NULL,
    created_at timestamp without time zone,
    created_by integer,
    updated_at timestamp without time zone,
    updated_by integer,
    CONSTRAINT city_pkey PRIMARY KEY (city_id),
    CONSTRAINT city_state_fk_key FOREIGN KEY (state_id)
                REFERENCES jippy_fm.state(state_id)
                ON UPDATE NO ACTION
                ON DELETE NO ACTION

