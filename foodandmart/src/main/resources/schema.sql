     CONSTRAINT product_outlet_categories_fk_key FOREIGN KEY (outlet_category_id)
                 REFERENCES jippy_fm.outlet_categories (outlet_category_id)
                 ON UPDATE NO ACTION
                 ON DELETE NO ACTION
);

